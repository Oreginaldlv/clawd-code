function sortEntries(a, b) {
  const da = a.isDirectory() ? 0 : 1;
  const db = b.isDirectory() ? 0 : 1;
  if (da !== db) return da - db;
  return a.name.localeCompare(b.name);
}

function isVisible(name) {
  return !name.startsWith(".");
}

async function main() {
  const entries = (await readdir(srcDir, { withFileTypes: true })).filter((e) =>
    isVisible(e.name),
  );
  entries.sort(sortEntries);

  const files = entries.filter((e) => e.isFile());
  const dirs = entries.filter((e) => e.isDirectory());

  let body = `---
outline: deep
---

# Generated \`src/\` index

This page is generated by \`npm run docs:gen\`. It lists top-level \`src/\` entries and **immediate** children of each directory so you can jump to paths in your editor (for example \`src/services/mcp/client.ts\`).

## Top-level files

| Path | Note |
|------|------|
`;

  for (const f of files) {
    const note = DESCRIPTIONS[f.name] ?? "—";
    body += `| \`src/${f.name}\` | ${note} |\n`;
  }

  body += `
## Top-level directories

`;

  for (const d of dirs) {
    const desc = DESCRIPTIONS[d.name] ?? "See architecture docs for role.";
    const dirPath = join(srcDir, d.name);
    body += `### \`src/${d.name}/\`\n\n${desc}\n\n`;

    const children = (await readdir(dirPath, { withFileTypes: true })).filter((c) =>
      isVisible(c.name),
    );
    children.sort(sortEntries);

    body += "| Name | Type |\n|------|------|\n";
    for (const c of children) {
      const type = c.isDirectory() ? "dir" : "file";
      body += `| \`${c.name}\` | ${type} |\n`;
    }
    body += "\n";
  }

  const banner = `<!-- This file is generated by scripts/generate-src-index.mjs -->\n\n`;
  await writeFile(outFile, banner + body, "utf8");
  console.log("Wrote", outFile);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
